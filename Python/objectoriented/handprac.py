# File Operation:

# File operations refer to the various actions or manipulations that can be performed on files in a computer system.
# 2. Common file operations include reading from a file, writing to a file, opening a file, closing a file, 
# seeking within a file, and deleting a file.

# Mode:

# In the context of file operations, the term "mode" typically refers to the permissions or the type of access that a program has
#  to a file.
# File modes determine whether a file is opened for 
# reading, writing, or both, and whether the file should be created if it doesn't exist.
# Common file modes include "r" for read, "w" for write, "a" for append, "b" for 
# binary mode, and combinations like "r+", "w+", and "a+" that allow both reading and writing.